def get_all_topics():
    """
    全単元とトピックのリストをフラットに取得する。
    """
    all_topics = []
    for unit, topics_dict in CURRICULUM.items():
        for topic, subtopics in topics_dict.items():
            for subtopic in subtopics:
                all_topics.append({
                    "unit": unit,
                    "topic": topic,
                    "subtopic": subtopic
                })
    return all_topics
